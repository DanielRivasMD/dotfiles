####################################################################################################
#!/usr/bin/env zsh
####################################################################################################

hx() {
  : "${HELIX_LOG:=$HOME/.cache/helix/logs/helix-$(date +%Y%m%d-%H%M%S)-$$.log}"
  mkdir -p "$(dirname "$HELIX_LOG")"
  command hx --log "$HELIX_LOG" "$@"
}

