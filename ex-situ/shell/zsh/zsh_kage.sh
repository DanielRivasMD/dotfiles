####################################################################################################
#!/usr/bin/env zsh
####################################################################################################

kage() {
  if [[ $# -eq 0 ]]; then
    local last_cmd=$(fc -ln -1 | sed -e 's/^[[:space:]]*//' -e 's/^kage[[:space:]]*//')
    if [[ -z "$last_cmd" ]]; then
      echo "Error: no previous command found" >&2
      return 1
    fi
    command kage sh -c "$last_cmd"
  else
    command kage "$@"
  fi
}

